package com.demo.inter.array;

public class TwoDArray {

	public static void main(String[] args) {
		int arr[][] = { { 2, 7, 9 }, { 3, 6, 1 } };
		for (int i = 0; i < 2; i++) {
			for (int j = 0; j < 3; j++) {
				System.out.print(arr[i][j]);
			}
			System.out.println();
		}
	}

	//array rotation by iteration
	// array rotation by rotation
	
}
